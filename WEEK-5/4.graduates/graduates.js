function graduates (students) {
  var obj = {};

  for (var i = 0; i < students.length; i++) {
    var kelas = students[i].class;
    obj[kelas] = [];
  }

  for (var j = 0; j < students.length; j++) {
    var kelas2 = students[j].class;
    if (students[j].score > 75) {
      obj[kelas2].push({name: students[j].name, score: students[j].score});
    }
  }

  return obj;
}

